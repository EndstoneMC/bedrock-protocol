"""Render a DraftField list into a Python DSL skeleton file."""

from __future__ import annotations

from dataclasses import dataclass

from .enums import EnumDraft
from .grouping import NestedClass
from .mappings import DraftField
from .version_chain import CLOUDBURST_FLOOR, ChainLink, PacketInfo


@dataclass
class PacketDraft:
    """Everything the emitter needs to render one packet."""

    info: PacketInfo
    chain: list[ChainLink]
    fields: list[DraftField]
    enums: list[EnumDraft]
    nested: list[NestedClass]


def render(draft: PacketDraft) -> str:
    """Build the .py source for one draft packet."""
    name = draft.info.name
    pkt_id = draft.info.packet_id

    since_kw = ""
    if draft.info.since > CLOUDBURST_FLOOR:
        since_kw = f", since={draft.info.since}"
    until_kw = ""
    if draft.info.until is not None:
        until_kw = f", until={draft.info.until}"

    versions = ",".join(f"v{link.version}" for link in draft.chain)

    all_fields = list(draft.fields) + [f for nc in draft.nested for f in nc.fields]
    imports = _collect_imports(all_fields)
    if draft.enums:
        imports.add("type")
    stdlib_imports = _collect_stdlib_imports(draft)

    lines: list[str] = []
    lines.append("# AUTOGENERATED DRAFT.  Before promoting to protocol/<name>.py:")
    lines.append("#   - rename every field/type from bedrock-headers (CLAUDE.md rule 12)")
    lines.append("#   - add packet/type docstrings from bedrock-protocol-docs (rule 11)")
    lines.append("#   - cross-check shape and enum-member since= against gophertunnel (rule 7)")
    lines.append("#   - add a golden round-trip test (rule 8)")
    lines.append(f"# Source: CloudburstMC serializer chain [{versions}]")
    lines.append("")
    for stmt in stdlib_imports:
        lines.append(stmt)
    if stdlib_imports:
        lines.append("")
    if imports:
        lines.append("from protocol import " + ", ".join(sorted(imports)))
        lines.append("")
    lines.append('package = "bedrock.protocol"')
    lines.append("")

    for enum in draft.enums:
        lines.append("")
        lines.extend(_render_enum(enum))

    for nested in draft.nested:
        lines.append("")
        lines.extend(_render_nested(nested))

    lines.append("")
    lines.append(f"@packet(id={pkt_id}{since_kw}{until_kw})")
    lines.append(f"class {name}:")
    lines.append('    """"""  # TODO: docstring from bedrock-protocol-docs')

    if not draft.fields:
        lines.append("    pass")
    else:
        used_names: set[str] = set()
        for f in draft.fields:
            for raw in _render_field_lines(f, used_names):
                lines.append("    " + raw)

    lines.append("")
    return "\n".join(lines)


def _collect_stdlib_imports(draft: PacketDraft) -> list[str]:
    """Return `from X import Y` lines needed by the draft body."""
    out: list[str] = []
    if draft.enums:
        out.append("from enum import IntEnum")
    needs_union = any(" | None" in f.dsl_type or f.extras.get("type") == "Union" for f in draft.fields)
    if needs_union:
        out.append("from typing import Union")
    needs_uuid = any("uuid.UUID" in f.dsl_type for f in draft.fields)
    if needs_uuid:
        out.append("import uuid")
    return out


def _render_nested(nested: NestedClass) -> list[str]:
    lines = [f"class {nested.name}:"]
    if not nested.fields:
        lines.append("    pass")
        return lines
    used: set[str] = set()
    for f in nested.fields:
        for raw in _render_field_lines(f, used):
            lines.append("    " + raw)
    return lines


def _render_enum(enum: EnumDraft) -> list[str]:
    """Render one IntEnum block. Members get explicit ordinal values either
    way (Java enum ordinals are the wire value when no explicit value is set).
    """
    lines: list[str] = []
    lines.append("@type  # TODO: confirm since= and add value(N, since=) gating")
    lines.append(f"class {enum.name}(IntEnum):")
    if not enum.members:
        lines.append("    pass")
        return lines
    auto = 0
    for member in enum.members:
        value = member.value if member.value is not None else auto
        auto = value + 1
        lines.append(f"    {member.name} = {value}")
    return lines


_PROTOCOL_SYMBOLS = {
    "varint32",
    "uvarint32",
    "varint64",
    "uvarint64",
    "int8",
    "int16",
    "int32",
    "int64",
    "uint8",
    "uint16",
    "uint32",
    "uint64",
    "float32",
    "double",
}


def _collect_imports(fields: list[DraftField]) -> set[str]:
    """Return the protocol-module symbols we need at the top of the file."""
    imports: set[str] = {"packet"}
    needs_field = False
    for f in fields:
        if f.dsl_type in _PROTOCOL_SYMBOLS:
            imports.add(f.dsl_type)
        if f.extras:
            needs_field = True
        # field(type=varint32, ...) references a wire-type symbol from the
        # extras dict — those need imports too.
        wire_ref = f.extras.get("type")
        if wire_ref in _PROTOCOL_SYMBOLS:
            imports.add(wire_ref)
    if needs_field:
        imports.add("field")
    return imports


def _render_field_lines(f: DraftField, used: set[str]) -> list[str]:
    """One DraftField may render as 1-N source lines (cond context as comment)."""
    out: list[str] = []
    if f.cond_chain:
        out.append("# CLOUDBURST_IMPORT_TODO: wrapped in " + " && ".join(f.cond_chain))

    name = _dedupe(f.name, used)
    used.add(name)

    # Order extras the way the hand-written DSL conventionally does:
    # type/prefix/endian/when first (wire-shape), then since/until/deprecated.
    order = ["type", "prefix", "endian", "when", "since", "until", "deprecated"]
    extras_parts: list[str] = []
    for k in order:
        if k in f.extras:
            extras_parts.append(f"{k}={f.extras[k]}")
    for k, v in f.extras.items():
        if k not in order:
            extras_parts.append(f"{k}={v}")
    rhs = ""
    if extras_parts:
        rhs = " = field(" + ", ".join(extras_parts) + ")"

    line = f"{name}: {f.dsl_type}{rhs}"
    if f.todo:
        line += f"  # {f.todo}"
    out.append(line)
    return out


def _dedupe(name: str, used: set[str]) -> str:
    if name not in used:
        return name
    i = 2
    while f"{name}_{i}" in used:
        i += 1
    return f"{name}_{i}"
